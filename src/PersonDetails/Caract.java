package PersonDetails;

public class Caract {
    private String personName;
    private String Puesto;
	public Caract() {
	
		
	}
	public void setName(String name){
		personName=name;
		
	}
	public String getName(){
		return personName;
		
	}
	
	
	
	
	public void setJob(String Job){
		Puesto=Job;
		
	}
	public String getJob(){
		return Puesto;
		
	}
	
	
	
}
